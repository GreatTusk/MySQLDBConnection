    private void llenarTabla() {
        //definicion variables para rescatar datos
        int id;
        String nombre, apellido, cargo;
        DefaultTableModel tblModelo = (DefaultTableModel) this.tblUsuarios.getModel();
        //para que no se dupliquela información
        tblModelo.setRowCount(0);
        ArrayList<Usuario> lista =  rs.lisrarUsuarios();
        for (Usuario u : lista) {
            id = u.getId();
            nombre = u.getNombre();
            apellido = u.getApellido();
            cargo = u.getCargo();
            tblModelo.addRow(new Object[]{id, nombre, apellido, cargo});
            
        }
        
    }
