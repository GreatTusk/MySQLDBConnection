    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        //Validar que los campos estén con info
        if(!this.txtID.getText().isEmpty()) {
            if(!this.txtNombre.getText().isEmpty()) {
                if(!this.txtApellido.getText().isEmpty()) {
                    this.u = rs.buscaPorID(Integer.parseInt(this.txtID.getText()));
                    if(u.getId() == 0) {
                        u.setId(Integer.parseInt(this.txtID.getText()));
                        u.setNombre(this.txtNombre.getText());
                        u.setApellido(this.txtApellido.getText());
                        u.setCargo(this.cboCargo.getSelectedItem().toString());
                        if(rs.agregarUsuario(u)) {
                            JOptionPane.showMessageDialog(rootPane, "Usuario Agregado con éxito", "Confirmación de datos", JOptionPane.INFORMATION_MESSAGE);
                            llenarTabla();
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Usuario no Agregado correctamente", "Confirmación de datos", JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Usuario ya existe", "Confirmación de datos", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Apellido es Obligatorio", "Confirmación de datos", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Nombre es Obligatorio", "Confirmación de datos", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Identificador es Obligatorio", "Confirmación de datos", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

